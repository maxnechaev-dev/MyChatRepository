import UIKit

extension UIColor {
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.8148267269, green: 0.008243261836, blue: 0.1024991497, alpha: 1)
    }
    
    static func mainWhite() -> UIColor {
        return #colorLiteral(red: 0.9718595147, green: 0.9763169885, blue: 0.9979617, alpha: 1)
    }
    
    static func buttonDark() -> UIColor {
        return #colorLiteral(red: 0.1960602105, green: 0.1960886121, blue: 0.1960505545, alpha: 1)
    }
    
    static func textFieldLight() -> UIColor {
        return #colorLiteral(red: 0.9018915296, green: 0.9019998312, blue: 0.9018546343, alpha: 1)
    }
}
